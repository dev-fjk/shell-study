#!/bin/sh
 
INPUT_FILE=./problem_fruits

## 2. ここを参考にINPUT_FILEを全行取得して表示するスクリプトを書いてみてください。
## https://qiita.com/r18j21/items/70018b98509309a0d2c5

## APPENDIX. 余裕があったらINPUT_FILEが存在しない場合に「該当ファイルがありません」という制御を追加してみてください。

if [ ! -e $INPUT_FILE ]; then
	echo "該当ファイルがありません"
	exit 
fi	

# ファイルの書き出し
while read tmpLine
do
	echo ${tmpLine}

done < $INPUT_FILE
